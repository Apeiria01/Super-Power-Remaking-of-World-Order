--POLICY_BILL_OF_RIGHTS
INSERT INTO Policy_BuildingClassYieldChanges (PolicyType, BuildingClassType, YieldType, YieldChange)
SELECT 'POLICY_BILL_OF_RIGHTS', t1.BuildingClass, 
(CASE WHEN t1.SpecialistType='SPECIALIST_ENGINEER'  THEN 'YIELD_PRODUCTION' 
      WHEN t1.SpecialistType='SPECIALIST_SCIENTIST' THEN 'YIELD_SCIENCE' 
      WHEN t1.SpecialistType='SPECIALIST_MERCHANT'  THEN 'YIELD_GOLD'
      WHEN (t1.SpecialistType='SPECIALIST_WRITER' OR t1.SpecialistType='SPECIALIST_MUSICIAN' OR t1.SpecialistType='SPECIALIST_ARTIST') 
      THEN 'YIELD_CULTURE'
      end),
t1.SpecialistCount
FROM Buildings t1 LEFT JOIN BuildingClasses t2
ON t2.Type = t1.BuildingClass
WHERE t1.Type =  t2.DefaultBuilding AND t1.SpecialistCount > 0 
AND (t1.SpecialistType = 'SPECIALIST_ENGINEER' OR t1.SpecialistType='SPECIALIST_SCIENTIST' OR t1.SpecialistType='SPECIALIST_MERCHANT'
or t1.SpecialistType='SPECIALIST_WRITER' OR t1.SpecialistType='SPECIALIST_MUSICIAN' OR t1.SpecialistType='SPECIALIST_ARTIST');


